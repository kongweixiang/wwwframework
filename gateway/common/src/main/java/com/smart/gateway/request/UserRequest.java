package com.smart.gateway.request;

public class UserRequest extends BaseRequest<UserRequest.Params> {

    public static class Params{
        private String id;
        private String name;

        public String getId() {
            return id;
        }

        public void setId(String id) {
            this.id = id;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }
    }
}
